#include <stdio.h>
unsigned char cmd[] = {0xad,0xac,0x0,0x25,0x03,0x00,0x1,0x0,0x0, 0xa1,0x18,0x1,0x21,0x23,0x45,0x17,0x57,0x45,0x41,0x44,0x46,0x45,0x08,0x66,0x29,0x70,0x34,0x50,0x73,0x97,0x08,0x37,0x41,0x0f,0x3a,0xd8,0x22};
unsigned int CRC_check(unsigned char *Data,unsigned short Data_length)
{
	unsigned int mid=0;
	unsigned char times=0;
	unsigned short Data_index=0;
	unsigned int CRC=0xFFFF;
	while(Data_length)
	{
		CRC=Data[Data_index]^CRC;
		for(times=0;times<8;times++)
		{
			mid=CRC;
			CRC=CRC>>1;
			if(mid & 0x0001)
			{
				CRC=CRC^0xA001;
			}
		}
		Data_index++;
		Data_length--;
	}
	return CRC;
}

int main(int argc, void *argv[])
{
	printf("CRC %x\r\n",CRC_check(cmd+2,sizeof(cmd)-4));
	return 0;
}
